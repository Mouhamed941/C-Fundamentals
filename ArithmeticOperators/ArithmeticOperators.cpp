
#include <iostream>
using namespace std;
int main()
{
    cout << "Please Enter The First Number :\n";
    short firstNumber;
    cin >> firstNumber;

    cout << "Please Enter The Seconde Number :\n";
    short secondeNumber;
    cin >> secondeNumber;

    cout << firstNumber << " + " << secondeNumber << " = " << firstNumber + secondeNumber << endl;
    cout << firstNumber << " - " << secondeNumber << " = " << firstNumber - secondeNumber << endl;;
    cout << firstNumber << " * " << secondeNumber << " = " << firstNumber * secondeNumber << endl;;
    cout << firstNumber << " / " << secondeNumber << " = " << firstNumber / secondeNumber << endl;;
    cout << firstNumber << " % " << secondeNumber << " = " << firstNumber % secondeNumber << endl;;

}


